//判断位置是否在棋盘
function IN_BOARD(sq){
	return IN_BOARD_[sq] != 0;
}

//判断是否在九宫格
function IN_FORT(sq){
	return IN_FORT_[sq] != 0;
}
function SQUARE_FLIP(sq) {
  return 254 - sq;
}
//根据一维数组，获取二维数组的行数
function RANK_Y(sq){
	return (sq >> 4);
}
//根据一维数组，获取二维数组的列数
function RANK_X(sq){
	return (sq & 15);
}
//将二维数组转化为一维数组
function COORD_XY(x,y){
	return x + (y << 4);
}
//根据fen串的值，获取对应的棋子
function CHAR_TO_PIECE(c) {
  switch (c) {
  case "K":
    return PIECE_KING;
  case "A":
    return PIECE_ADVISOR;
  case "B":
    return PIECE_BISHOP;
  case "N":
    return PIECE_KNIGHT;
  case "R":
    return PIECE_ROOK;
  case "C":
    return PIECE_CANNON;
  case "P":
    return PIECE_PAWN;
  default:
    return -1;
  }
}
//根据当前下棋方，设置红黑标记 return 8 : 红子 return 16 黑子
function SIDE_TAG(sd){
	return 8 + (sd << 3);
}
//对方的红黑标记
function OPP_SIDE_TAG(sd){
	return 16 - (sd << 3);
}
//将棋子的起点和终点转换为一个走法
function MOVE(sqSrc,sqDst){
	return sqSrc + (sqDst << 8);
}
//根据走法获取对应的起点和终点
function SRC(mv){
	return (mv & 255);
}
function DST(mv){
	return (mv >> 8);
}

//兵（卒）前进一步时的终点，玩家都是 -16；电脑都是 +16
function SQUARE_FORWARD(sq,sd){
	var temp = sd == COMPUTER ? 1 : 0;
	return sq - 16 + (temp << 5);
}
//兵(卒)是否过河:只考虑玩家，若玩家为红方-0，过河时sq 第八位为0；玩家为黑方-1，过河时第八位为 0
function AWAY_HALF(sq,sd){
	var temp = sd == COMPUTER ? 1 : 0;
	return (sq & 0x80) == (temp << 7);
}

//将(帅)的走法是否合法
function KING_SPAN(sqSrc,sqDst){
	return LEGAL_SPAN[sqDst - sqSrc + 256] == 1;
}
//士的走法是否合法
function ADVISOR_SPAN(sqSrc,sqDst){
	return LEGAL_SPAN[sqDst - sqSrc + 256] == 2;
}
//象(相)的走法是否合法
function BISHOP_SPAN(sqSrc,sqDst){
	return LEGAL_SPAN[sqDst - sqSrc + 256] == 3;
}
//象(相)是否过河,如果过河，则起点和终点会分布在（0-127）和（128-255）
function SAME_HALF(sqSrc,sqDst){
	return ((sqSrc ^ sqDst) & 0x80) == 0;
}
//在起点和终点均合法的情况下，返回象眼位置
function BISHOP_PIN(sqSrc,sqDst){
	return (sqSrc + sqDst) >> 1;
}
//判断马的位置是否合法,并返回马脚的位置,走位不合法时，返回起点
function KNIGHT_SPAN(sqSrc,sqDst){
	return KNIGHT_PIN_[sqDst - sqSrc + 256] + sqSrc;
}
//判断起点和终点是否同一行, 同一行，最后四位不同
function SAME_RANK(sqSrc,sqDst){
	return ((sqSrc ^ sqDst) & 0xf0) == 0;
}
//判断起点和终点是否同一列
function SAME_FILE(sqSrc,sqDst){
	return ((sqSrc ^ sqDst) & 0x0f) == 0;
}